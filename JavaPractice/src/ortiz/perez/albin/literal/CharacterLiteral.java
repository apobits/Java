package ortiz.perez.albin.literal;

/**
 * @author Administrador
 *
 */
public class CharacterLiteral {

	public static void main(String[] args) {

		// character literal
		char a = 'a';

		// character literal of integer representation
		char b = 98;

		System.out.println(a + " " + b);

		// character literal with special character
		char newLine = '\n';

		System.out.println("\"R " + newLine + "R\"");

	}

}
